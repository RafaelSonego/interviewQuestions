  `PK_USER` mediumint(8) NOT NULL AUTO_INCREMENT,
  `DS_LOGIN` varchar(20) NOT NULL,
  `DS_PASSWORD` varchar(20) NOT NULL,
  `DS_EMAIL` varchar(255) NOT NULL,
  `DS_FIRSTNAME` varchar(30) NOT NULL,
  `DS_LASTNAME` varchar(100) NOT NULL,
  `DT_BIRTHDAY` date NOT NULL,
  `DT_CREATE` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP,
  `DT_UPDATE` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (`PK_USER`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;

CREATE TABLE `TBL_QUESTION` (
  `PK_QUESTION` mediumint(8) NOT NULL AUTO_INCREMENT,
  `DS_QUESTION` varchar(600) NOT NULL,
  `DS_ANSWER` varchar(4000) NOT NULL,
  `DS_ATTACHED` blob,
  `FK_TECHNOLOGY` mediumint(8) NOT NULL,
  `FK_USER` mediumint(8) NOT NULL,
  `DT_CREATE` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP,
  `DT_UPDATE` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (`PK_QUESTION`),
  KEY `FK_TECHNOLOGY` (`FK_TECHNOLOGY`),
  KEY `FK_USER` (`FK_USER`),
